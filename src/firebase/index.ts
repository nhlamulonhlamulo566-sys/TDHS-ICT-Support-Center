
'use client';

import { firebaseConfig } from '@/firebase/config';
import { initializeApp, getApps, getApp, type FirebaseApp } from 'firebase/app';
import { getAuth, type Auth } from 'firebase/auth';
import { getFirestore, type Firestore } from 'firebase/firestore';

let firebaseApp: FirebaseApp;
let auth: Auth;
let firestore: Firestore;

/**
 * Initializes Firebase services using a more robust singleton pattern.
 * This ensures that Firebase is initialized only once, preventing race conditions
 * and internal state errors in Next.js environments.
 * @returns The initialized Firebase app, auth, and firestore services.
 */
export function initializeFirebase() {
  if (getApps().length === 0) {
    firebaseApp = initializeApp(firebaseConfig);
  } else {
    firebaseApp = getApp();
  }
  auth = getAuth(firebaseApp);
  
  // Use getFirestore for a more robust initialization pattern
  firestore = getFirestore(firebaseApp);

  return { firebaseApp, auth, firestore };
}

